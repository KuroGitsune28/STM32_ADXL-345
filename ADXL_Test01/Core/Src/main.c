void adxl_write(uint8_t reg, uint8_t value)
{
	uint8_t Data[2];
	Data[0] = reg;
	Data[1] = value;
	if( HAL_I2C_Master_Transmit(&hi2c2, ADXL_ADDR, Data , 2, 100) == HAL_OK)
	{
		printf("OK\r\n");
	}
}

void adxl_read(uint8_t reg , uint8_t Noofbyte)
{

	if(HAL_I2C_Mem_Read(&hi2c2, ADXL_ADDR, reg, 1, data_rec, Noofbyte, 100) != HAL_OK)
	{
		printf("Error\r\n");
		Error_Handler();
	}
}

void adxl_init(void)
{
	adxl_read(0x00, 1);

	// Power Register
	adxl_write(0x2D, 0x00); // reset power
	adxl_write(0x2D, 0x08); // measure mode

	// data format
	adxl_write(0x31,0x01); // +-4g

}
